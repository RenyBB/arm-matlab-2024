% a 12 x 1 matrix of numbers in uniform distribution of [0, 1)
rand(12,1) 

% a 12 x 1 matrix of numbers from a normal distribution 
% with mean 0 and variance 1
randn(12,1) 

% a 12 x 1 matrix of random integers between 1 and 10
randi(10,12,1) 

% 10 unique random numbers between 1 and 12
randperm(12,10) 

% a 12 x 1 matrix filled with zeros
zeros(12,1) 
